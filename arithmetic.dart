void main() {
  int a = 10;
  int b = 5;

 
  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  double quotient = a / b;

  print('The sum of $a and $b is: $sum');
  print('The difference between $a and $b is: $difference');
  print('The product of $a and $b is: $product');
  print('The quotient of $a divided by $b is: $quotient');
}
